    $(document).ready(function () {
        // modernizr
        function checkModernizr() {
            if (Modernizr.mq('(min-width: 480px)')) {
                popoverTrig();
            }
            if (Modernizr.mq('(max-width: 479px)')) {
                $('.popper').popover('destroy');
            }
        }

        // the call to checkMq here will execute after the document has loaded
        checkModernizr();

        $(window).resize(function () {
            checkModernizr();
        });
        //hero images slider
        $('.hero-carousel').slick({
            dots: true,
            infinite: true,
            speed: 500,
            fade: true,
            arrows: false,
            slidesToShow: 1,
            cssEase: 'linear',
            autoplay: true,
        });

        //dvd covers slider
        $('.home-carousel').slick({
            dots: false,
            infinite: false,
            speed: 200,
            slidesToShow: 5,
            slidesToScroll: 1,
            responsive: [
                {
                    breakpoint: 980,
                    settings: {
                        slidesToShow: 3,
                        slidesToScroll: 1,
                    }
                },
                {
                    breakpoint: 600,
                    settings: {
                        slidesToShow: 2,
                        slidesToScroll: 1
                    }
                },
                {
                    breakpoint: 479,
                    settings: {
                        slidesToShow: 1,
                        slidesToScroll: 1
                    }
                }
            ]
        })

        //raty stars
        $('.slide-score').raty({
            readOnly: false,
            score: function () {
                return $(this).attr('data-score');
            }
        });
        $("i.fa-fw").on('click', function () {
            var score = $(this).attr("data-score");
            alert(score);
            //save to database
        });

        //dvd slider popover
        function popoverTrig() {
            var originalLeave = $.fn.popover.Constructor.prototype.leave;
            $.fn.popover.Constructor.prototype.leave = function (obj) {
                var self = obj instanceof this.constructor ? obj : $(obj.currentTarget)[this.type](this.getDelegateOptions()).data('bs.' + this.type);
                originalLeave.call(this, obj);

                if (obj.currentTarget) {
                    self.$tip.one('mouseenter', function () {
                        clearTimeout(self.timeout);
                        self.$tip.one('mouseleave', function () {
                            $.fn.popover.Constructor.prototype.leave.call(self, self);
                        });
                    })
                }
            };
            $('.popper').popover({
                placement: 'auto left',
                trigger: 'hover',
                container: 'body',
                html: true,
                delay: {
                    show: 300,
                    hide: 100
                },
                content: function () {
                    return $(this).next('.cover-overlay').html();
                }
            }).click(function(e) {
                e.preventDefault() ;
            }).mouseenter(function(e) {
                $(this).popover('show');
            });


            // synopsis adaptive trigg open
            $('.synopsys-adaptive-open').on('click', function () {
                $('.synopsys-adaptive-open').removeClass('hidden');
                $(this).addClass('hidden');
                $('.synopsys-content').removeClass('on');
                $(this).prev('.synopsys-content').addClass('on');
                return false;
            })

            // synopsis adaptive trigg close
            $('.synopsys-adaptive-close').on('click', function () {
                $(this).closest('.slick-slide').children('.synopsys-adaptive-open').removeClass('hidden');
                $(this).closest('.grid-slide').children('.synopsys-adaptive-open').removeClass('hidden');
                $(this).closest('.synopsys-content').removeClass('on');

                return false;
            })

        }
    })
</script><!--/SCRIPTS-->